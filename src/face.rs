#[derive(Debug, Clone)]
pub enum Direction {
    North,
    Northeast,
    East,
    Southeast,
    South,
    Southwest,
    West,
    Northwest,
}

impl Direction {
    pub fn is_orthogonal(&self) -> bool {
        match self {
            Direction::North => true,
            Direction::Northeast => false,
            Direction::East => true,
            Direction::Southeast => false,
            Direction::South => true,
            Direction::Southwest => false,
            Direction::West => true,
            Direction::Northwest => false,
        }
    }
}

#[derive(Debug, Clone)]
pub enum Move {
    Step(u32, Direction),
    Range(Direction),
    Jump(u32, Direction),
    Dove,
    KnightForward,
    KnightBackward,
    Hook(Direction),
    FullLion,
    LimitedLion,
    JumpThenRange(u32, Direction),
    FlyingJump(Direction),
    FlyingCapture(Direction),
    JumpOrRange(u32, Direction),
}

